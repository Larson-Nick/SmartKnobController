Hall::Hall(ADC_HandleTypeDef* hadc)
	:
	  hadc(hadc)
{}

int16_t Hall::getReading(uint16_t angle) {
	// adjust the reading compensate for the offset caused by the encoder magnet
    uint16_t raw = readADC();
    uint16_t offset = offsetTable[angle];
    int16_t reading = (int32_t)raw - (int32_t)offset;
    return reading;
}

uint16_t Hall::readADC() {
	// read the ADC value while disabling the interrupt
	 __disable_irq();
    HAL_ADC_Start(hadc);
    HAL_ADC_PollForConversion(hadc, 10);
    uint16_t val = HAL_ADC_GetValue(hadc);
    __enable_irq();

    return val;
}

